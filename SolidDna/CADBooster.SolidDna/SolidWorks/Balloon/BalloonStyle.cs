namespace CADBooster.SolidDna
{
    /// <summary>
    /// The style of a balloon
    /// </summary>
    public enum BalloonStyle
    {
        /// <summary>
        /// Use the document layout default
        /// </summary>
        DocumentDefault = -1,

        /// <summary>
        /// Circular
        /// </summary>
        Circular = 1,

        /// <summary>
        /// Triangle
        /// </summary>
        Triangle = 2,

        /// <summary>
        /// Hexagon
        /// </summary>
        Hexagon = 3,

        /// <summary>
        /// Box
        /// </summary>
        Box = 4,

        /// <summary>
        /// Diamond
        /// </summary>
        Diamond = 5,

        /// <summary>
        /// Pentagon. Can be used for label location selection Circular Spit Line
        /// </summary>
        Pentagon = 6,

        /// <summary>
        /// Split circle. Not valid for notes; only valid for balloons
        /// </summary>
        SplitCircle = 7,

        /// <summary>
        /// Flag pentagon
        /// </summary>
        FlagPentagon = 8,

        /// <summary>
        /// Flag triangle
        /// </summary>
        FlagTriangle = 9,

        /// <summary>
        /// Underline
        /// </summary>
        Underline = 10,

        /// <summary>
        /// Square
        /// </summary>
        Square = 11,

        /// <summary>
        /// Square circle
        /// </summary>
        SquareCircle = 12,

        /// <summary>
        /// Inspection
        /// </summary>
        Inspection = 13,

        /// <summary>
        /// Arc bracket
        /// </summary>
        ArcBracket = 14,

        /// <summary>
        /// Rectangle bracket
        /// </summary>
        RectangleBracket = 15,

        /// <summary>
        /// Arc length symbol
        /// </summary>
        ArcLengthSymbol = 16,

        /// <summary>
        /// Fixed symbol
        /// </summary>
        FixedSymbol = 17,

        /// <summary>
        /// Double arrow
        /// </summary>
        DoubleArrow = 18,

        /// <summary>
        /// Split square. Can be used for label location selection Square Spit Line
        /// </summary>
        SplitSquare = 19,

        /// <summary>
        /// Verbose
        /// </summary>
        Verbose = 20
    }
}
